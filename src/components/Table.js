import React from "react";

function Table({ totalEaten, currentBalance }) {
  const emptyPlates = Array.from({ length: totalEaten }, (_, index) => (
    <div key={index} className="empty-plate" style={{ top: -7 * index }} />
  ));

  return (
    <>
      <h1 className="remaining">
        You have: ${currentBalance} remaining!
      </h1>
      <div className="table">
        <div className="stack">{emptyPlates}</div>
      </div>
    </>
  );
}

export default Table;
